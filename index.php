<script>
function animateCount(id, target, duration) {
  let start = 0;
  const element = document.getElementById(id);
  const stepTime = Math.abs(Math.floor(duration / target)); 
  const timer = setInterval(() => {
    start += 1;
    element.textContent = start;
    if (start >= target) {
      clearInterval(timer);
    }
  }, stepTime);
}

